export function getPortById(id: string): Port | undefined {
  const allPorts = [
    ...chinesePorts,
    ...chineseAirports,
    ...Object.values(destinationPorts).flat(),
  ];
  return allPorts.find((port) => port.id === id);
}

export function getPortsByCountry(countrySlug: string): Port[] {
  return destinationPorts[countrySlug] || [];
}
